
public class TesteWhile {
	
	public static void main(String[] args) {
		
		int contador = 5;

        while(contador <= 10) {

        	System.out.println("Antes do + 1 " + contador);

			contador = contador + 1;
       
        	System.out.println("Depois do + 1 " + contador);
        	
        }
    }
}