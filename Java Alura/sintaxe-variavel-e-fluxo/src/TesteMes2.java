
public class TesteMes2 {

	public static void main(String[] args) {

		int mes = 4;

		switch (mes) {
		case 1:
			System.out.println("Mes de Janeiro");
			break;
		case 2:
			System.out.println("Mes de Fevereiro");
			break;
		case 3:
			System.out.println("Mes de Marco");
			break;
		default:
			System.out.println("Mes Ivalido");
			break;

		}

	}

}
